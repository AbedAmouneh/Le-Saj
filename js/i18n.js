/* ─────────────────────────────────────────────────
   I18N ENGINE
───────────────────────────────────────────────── */

const I18N = {
  currentLang: 'en',

  /**
   * Initialize i18n: load saved lang, render switcher, apply translations
   */
  init() {
    this.currentLang = localStorage.getItem('lessaj_lang') || 'en';
    this.applyLang(this.currentLang);
    this.renderSwitcher();
  },

  /**
   * Apply a language: set dir, update html[lang], translate all elements
   */
  applyLang(lang) {
    if (!translations[lang]) lang = 'en';
    this.currentLang = lang;
    localStorage.setItem('lessaj_lang', lang);

    // Direction
    const isRTL = lang === 'ar';
    document.documentElement.setAttribute('lang', lang);
    document.documentElement.setAttribute('dir', isRTL ? 'rtl' : 'ltr');

    // Translate text content
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const val = this.t(key);
      if (val) el.textContent = val;
    });

    // Translate placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const key = el.getAttribute('data-i18n-placeholder');
      const val = this.t(key);
      if (val) el.setAttribute('placeholder', val);
    });

    // Translate aria-labels
    document.querySelectorAll('[data-i18n-aria]').forEach(el => {
      const key = el.getAttribute('data-i18n-aria');
      const val = this.t(key);
      if (val) el.setAttribute('aria-label', val);
    });

    // Update switcher button states
    document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.classList.toggle('active', btn.dataset.lang === lang);
    });
  },

  /**
   * Get translation string, fallback to English
   */
  t(key) {
    return (translations[this.currentLang] && translations[this.currentLang][key])
      || (translations.en && translations.en[key])
      || key;
  },

  /**
   * Render language switcher into every nav
   */
  renderSwitcher() {
    const switchers = document.querySelectorAll('.lang-switcher');
    if (!switchers.length) return;
    switchers.forEach(sw => {
      sw.innerHTML = `
        <button class="lang-btn${this.currentLang === 'en' ? ' active' : ''}" data-lang="en" aria-label="English">EN</button>
        <button class="lang-btn${this.currentLang === 'ar' ? ' active' : ''}" data-lang="ar" aria-label="عربي">AR</button>
        <button class="lang-btn${this.currentLang === 'fr' ? ' active' : ''}" data-lang="fr" aria-label="Français">FR</button>
      `;
      sw.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', () => this.applyLang(btn.dataset.lang));
      });
    });
  }
};

// Auto-init when DOM is ready
if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', () => I18N.init());
} else {
  I18N.init();
}
